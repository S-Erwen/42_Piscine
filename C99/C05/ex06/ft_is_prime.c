int	ft_is_prime(int nb)
{
	long int u;
	long int i;

	i = 2;
	u = nb;
	if (u < 2)
		return (0);
	while (i * i <= u)
	{
		if (u % i == 0)
			return (0);
		i++;
	}
	return (1);
}
